DROP TABLE IF EXISTS active_sessions CASCADE;
DROP TABLE IF EXISTS intrusion_events CASCADE;
DROP TABLE IF EXISTS login_audit CASCADE;
DROP TABLE IF EXISTS staff_auth CASCADE;
DROP TABLE IF EXISTS role_permissions CASCADE;
DROP TABLE IF EXISTS permissions CASCADE;
DROP TABLE IF EXISTS roles CASCADE;

CREATE TABLE roles (
    role_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_name text NOT NULL UNIQUE,
    description text,
    created_at timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE permissions (
    permission_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    permission_code text NOT NULL UNIQUE,
    description text
);

CREATE TABLE role_permissions (
    role_id integer NOT NULL REFERENCES roles(role_id) ON DELETE CASCADE,
    permission_id integer NOT NULL REFERENCES permissions(permission_id) ON DELETE CASCADE,
    PRIMARY KEY (role_id, permission_id)
);

CREATE TABLE staff_auth (
    staff_id integer PRIMARY KEY REFERENCES staff(staff_id) ON DELETE CASCADE,
    role_id integer NOT NULL REFERENCES roles(role_id),
    password_hash text NOT NULL,
    must_change_password boolean NOT NULL DEFAULT false,
    failed_attempts integer NOT NULL DEFAULT 0,
    locked_until timestamp with time zone,
    last_login timestamp with time zone,
    is_active boolean NOT NULL DEFAULT true,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    updated_at timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE login_audit (
    login_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_id integer REFERENCES staff(staff_id) ON DELETE SET NULL,
    username text NOT NULL,
    ip_address text,
    user_agent text,
    success boolean NOT NULL,
    reason text,
    attempted_at timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE active_sessions (
    session_id text PRIMARY KEY,
    staff_id integer NOT NULL REFERENCES staff_auth(staff_id) ON DELETE CASCADE,
    ip_address text,
    user_agent text,
    created_at timestamp with time zone NOT NULL DEFAULT now(),
    expires_at timestamp with time zone NOT NULL,
    is_revoked boolean NOT NULL DEFAULT false
);

CREATE INDEX idx_active_sessions_monitoring
ON active_sessions(expires_at, is_revoked);

CREATE TABLE intrusion_events (
    intrusion_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username text,
    ip_address text,
    severity text NOT NULL DEFAULT 'MEDIUM',
    reason text NOT NULL,
    blocked_until timestamp with time zone,
    status text NOT NULL DEFAULT 'OPEN',
    created_at timestamp with time zone NOT NULL DEFAULT now()
);

