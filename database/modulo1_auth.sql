CREATE OR REPLACE FUNCTION update_staff_auth_updated_at()
RETURNS trigger AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_staff_auth_updated_at ON staff_auth;

CREATE TRIGGER trg_staff_auth_updated_at
BEFORE UPDATE ON staff_auth
FOR EACH ROW
EXECUTE FUNCTION update_staff_auth_updated_at();